    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        
        try {
            PreparedStatement ps=cn.prepareStatement("INSERT INTO empleados (nombre, app, apm, tel) VALUES (?,?,?,?)");
            ps.setString(1, txtnombre.getText());
            ps.setString(2, txtapp.getText());
            ps.setString(3, txtapm.getText());
            ps.setString(4, txttel.getText());
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "datos guardados");
            
            limpiar();
            mostrartabla("");
        } catch (Exception e) {
            System.err.println("Error al guardar:"+e);
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla");
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnactuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactuActionPerformed
        
        try {
            PreparedStatement ps=cn.prepareStatement("UPDATE empleados set nombre='"+txtnombre.getText()+"',app='"+txtapp.getText()+"',apm='"+txtapm.getText()+"',tel='"+txttel.getText()+"' WHERE id_empleado='"+txtid.getText()+"'");
            int respuesta = ps.executeUpdate();
            if (respuesta>0){
                JOptionPane.showMessageDialog(null, "datos actualizados");
                limpiar();
                mostrartabla("");
            }else{
                JOptionPane.showMessageDialog(null, "No ha seleccionaod filas");
            }
        } catch (SQLException e) {
            System.err.println("Error al actualizar: "+e);
            JOptionPane.showMessageDialog(null, "Error al actualizar");
        }
    }//GEN-LAST:event_btnactuActionPerformed

