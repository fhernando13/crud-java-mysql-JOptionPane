    void mostrartabla(String valor){
    
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("A. Paterno");
        modelo.addColumn("A. Materno");
        modelo.addColumn("Tel");
        
        tabla.setModel(modelo);
        
        String sql="select * from empleados where concat (nombre,' ',app) like '%"+valor+"%'";
        String datos[] = new String[5];
        Statement st;
        
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
                while(rs.next()){
                    datos[0]=rs.getString(1);
                    datos[1]=rs.getString(2);
                    datos[2]=rs.getString(3);
                    datos[3]=rs.getString(4);
                    datos[4]=rs.getString(5);
                    
                    modelo.addRow(datos);
                }
                tabla.setModel(modelo);
        } catch (SQLException e) {
            System.err.println("Error en el llenado de la tabla");
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla");
        }
        
    }

